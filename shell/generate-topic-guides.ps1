for ($i = 0; $i -lt $lines.Count; $i++) {
    if ($lines[$i] -match '^\s*\|\s*Level') { $startIdx = $i; break }
}
Write-Host "Python startIdx=$startIdx"
if ($startIdx -ge 0) {
    for ($i = $startIdx + 2; $i -lt $lines.Count; $i++) {
        $line = $lines[$i]
        if ($line -notmatch '^\s*\|') { break }
        $cols = $line.Trim('|') -split '\s*\|\s*'
        $levelStr = $cols[1].Trim()
        if (-not ($levelStr -match '^\\d+$')) { continue }
        $level = [int]$levelStr
        $topic = $cols[3].Trim()
        $official = $topic
        $url = $cols[4].Trim()
        Write-Host "Python processing Level=$level Topic=$topic"
        New-GuideFile -BaseDir "docs/02.Python3.12" -Level $level -Topic $topic -Official $official -Url $url
    }
}
